#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import YAML from 'yaml';

const DESIGN_PATH = 'DESIGN.md';
const GLOBAL_OUTPUT_PATH = 'src/app/design-tokens.css';
const BUTTON_OUTPUT_PATH = 'src/components/Button/Button.tokens.css';
const CHECK_MODE = process.argv.includes('--check');
const REFERENCE_PATTERN = /\{([^}]+)\}/g;

const PROPERTY_ALIASES = {
  backgroundColor: 'background-color',
  textColor: 'color',
  rounded: 'border-radius',
};

const TYPOGRAPHY_PROPERTIES = ['fontFamily', 'fontSize', 'fontWeight', 'lineHeight', 'letterSpacing'];

function readDesignTokens() {
  const markdown = fs.readFileSync(DESIGN_PATH, 'utf8');
  const frontMatter = extractFrontMatter(markdown);
  const parsed = YAML.parse(frontMatter);
  if (!parsed || typeof parsed !== 'object') {
    throw new Error(`${DESIGN_PATH} front matter did not parse to an object`);
  }
  return resolveAll(parsed);
}

function extractFrontMatter(markdown) {
  if (!markdown.startsWith('---\n')) {
    throw new Error(`${DESIGN_PATH} must start with YAML front matter`);
  }
  const end = markdown.indexOf('\n---\n', 4);
  if (end === -1) {
    throw new Error(`${DESIGN_PATH} front matter is missing closing delimiter`);
  }
  return markdown.slice(4, end);
}

function resolveAll(root) {
  const cache = new Map();

  function resolveByPath(referencePath, stack = []) {
    if (cache.has(referencePath)) return cache.get(referencePath);
    if (stack.includes(referencePath)) {
      throw new Error(`Circular design token reference: ${[...stack, referencePath].join(' -> ')}`);
    }
    const value = getByPath(root, referencePath);
    const resolved = resolveValue(value, [...stack, referencePath]);
    cache.set(referencePath, resolved);
    return resolved;
  }

  function resolveValue(value, stack = []) {
    if (typeof value === 'string') {
      const trimmed = value.trim();
      const exactReference = trimmed.match(/^\{([^}]+)\}$/);
      if (exactReference) return resolveByPath(exactReference[1], stack);
      return value.replace(REFERENCE_PATTERN, (_match, referencePath) => {
        const resolved = resolveByPath(referencePath, stack);
        if (resolved && typeof resolved === 'object') {
          throw new Error(`Token reference {${referencePath}} resolves to an object inside a string`);
        }
        return cssValue(resolved);
      });
    }

    if (Array.isArray(value)) {
      return value.map((item) => resolveValue(item, stack));
    }

    if (value && typeof value === 'object') {
      return Object.fromEntries(Object.entries(value).map(([key, item]) => [key, resolveValue(item, stack)]));
    }

    return value;
  }

  return resolveValue(root);
}

function getByPath(root, referencePath) {
  const parts = referencePath.split('.');
  let current = root;
  for (const part of parts) {
    if (!current || typeof current !== 'object' || !(part in current)) {
      throw new Error(`Unknown design token reference: {${referencePath}}`);
    }
    current = current[part];
  }
  return current;
}

function generatedHeader(source = DESIGN_PATH) {
  return [
    '/*',
    ` * Generated by scripts/generate-design-tokens.mjs from ${source}.`,
    ' * Do not edit directly; update DESIGN.md and rerun npm run design:tokens.',
    ' */',
    '',
  ];
}

function buildGlobalCss(tokens) {
  const lines = [...generatedHeader(), ':root {'];
  addSection(lines, 'Colors');
  for (const [name, value] of Object.entries(tokens.colors || {})) {
    addVar(lines, `design-color-${name}`, value);
  }
  addColorAliases(lines, tokens.colors || {});

  addSection(lines, 'Typography');
  for (const [name, typography] of Object.entries(tokens.typography || {})) {
    for (const property of TYPOGRAPHY_PROPERTIES) {
      if (property in typography) {
        addVar(lines, `design-typography-${name}-${toKebab(property)}`, typography[property]);
      }
    }
  }

  addSection(lines, 'Spacing');
  for (const [name, value] of Object.entries(tokens.spacing || {})) {
    addVar(lines, `design-spacing-${name}`, value);
  }

  addSection(lines, 'Radius');
  for (const [name, value] of Object.entries(tokens.rounded || {})) {
    addVar(lines, `design-radius-${name}`, value);
  }

  addSection(lines, 'Borders');
  for (const [name, border] of Object.entries(tokens.borders || {})) {
    if (border && typeof border === 'object') {
      for (const [property, value] of Object.entries(border)) {
        addVar(lines, `design-border-${name}-${toKebab(property)}`, value);
      }
      if ('width' in border && 'style' in border && 'color' in border) {
        addVar(lines, `design-border-${name}`, `${cssValue(border.width)} ${cssValue(border.style)} ${cssValue(border.color)}`);
      }
    } else {
      addVar(lines, `design-border-${name}`, border);
    }
  }

  addSection(lines, 'Focus');
  for (const [name, focus] of Object.entries(tokens.focus || {})) {
    if (focus && typeof focus === 'object') {
      for (const [property, value] of Object.entries(focus)) {
        addVar(lines, `design-focus-${name}-${toKebab(property)}`, value);
      }
      if ('width' in focus && 'color' in focus) {
        addVar(lines, `design-focus-${name}`, `${cssValue(focus.width)} solid ${cssValue(focus.color)}`);
      }
    } else {
      addVar(lines, `design-focus-${name}`, focus);
    }
  }

  addSection(lines, 'Layers');
  for (const [name, value] of Object.entries(tokens.layers || {})) {
    addVar(lines, `design-layer-${name}`, value);
  }

  addSection(lines, 'Shadows');
  for (const [name, value] of Object.entries(tokens.shadows || {})) {
    addVar(lines, `design-shadow-${name}`, value);
  }

  addSection(lines, 'Components');
  for (const [name, component] of Object.entries(tokens.components || {})) {
    addComponentVars(lines, name, component, `design-component-${name}`);
  }

  lines.push('}', '');
  return `${lines.join('\n')}\n`;
}

function buildButtonCss(tokens) {
  const button = tokens.components?.['button-primary'] || {};
  const secondary = tokens.components?.['button-secondary'] || {};
  const outline = tokens.components?.['button-outline'] || {};
  const destructive = tokens.components?.['button-destructive'] || {};
  const sizeSm = tokens.components?.['button-size-sm'] || {};
  const sizeMd = tokens.components?.['button-size-md'] || {};
  const sizeLg = tokens.components?.['button-size-lg'] || {};
  const buttonTypography = button.typography || {};

  const lines = [...generatedHeader(), ':root {'];
  addVar(lines, 'button-gap', tokens.spacing?.['2']);
  addVar(lines, 'button-border-width', tokens.borders?.default?.width);
  addVar(lines, 'button-border-radius', tokens.rounded?.['control-sm'] || button.rounded || tokens.rounded?.control);
  addVar(lines, 'button-font-family', buttonTypography.fontFamily);
  addVar(lines, 'button-font-weight', buttonTypography.fontWeight);
  addVar(lines, 'button-focus-ring', tokens.focus?.ring ? `${cssValue(tokens.focus.ring.width)} solid ${cssValue(tokens.focus.ring.color)}` : null);
  addVar(lines, 'button-focus-ring-offset', tokens.focus?.ring?.offset);
  addVar(lines, 'button-height-sm', sizeSm.height);
  addVar(lines, 'button-padding-sm', sizeSm.padding);
  addVar(lines, 'button-font-size-sm', sizeSm.typography?.fontSize || sizeSm.fontSize);
  addVar(lines, 'button-height-md', sizeMd.height || button.height);
  addVar(lines, 'button-padding-md', sizeMd.padding);
  addVar(lines, 'button-font-size-md', sizeMd.typography?.fontSize || sizeMd.fontSize);
  addVar(lines, 'button-height-lg', sizeLg.height);
  addVar(lines, 'button-padding-lg', sizeLg.padding);
  addVar(lines, 'button-font-size-lg', sizeLg.typography?.fontSize || sizeLg.fontSize);
  addVar(lines, 'color-focus-ring', tokens.focus?.ring?.color || tokens.colors?.['focus-ring']);
  addVar(lines, 'color-primary', button.backgroundColor || tokens.colors?.primary);
  addVar(lines, 'color-primary-hover', tokens.components?.['button-primary-hover']?.backgroundColor || tokens.colors?.['primary-strong']);
  addVar(lines, 'color-primary-active', tokens.colors?.['primary-strong']);
  addVar(lines, 'color-secondary', secondary.backgroundColor || tokens.colors?.['surface-subtle']);
  addVar(lines, 'color-secondary-hover', tokens.colors?.['border-soft']);
  addVar(lines, 'color-secondary-active', tokens.colors?.border);
  addVar(lines, 'color-secondary-text', secondary.textColor || tokens.colors?.['on-surface']);
  addVar(lines, 'color-text', secondary.textColor || tokens.colors?.['on-surface']);
  addVar(lines, 'color-hover-bg', tokens.colors?.['surface-muted']);
  addVar(lines, 'color-active-bg', tokens.colors?.['surface-subtle']);
  addVar(lines, 'color-danger', destructive.backgroundColor || tokens.colors?.danger);
  addVar(lines, 'color-danger-hover', tokens.colors?.['danger-text']);
  addVar(lines, 'color-danger-active', tokens.colors?.['danger-text']);
  addVar(lines, 'color-on-primary', button.textColor || tokens.colors?.surface);
  addVar(lines, 'color-on-danger', destructive.textColor || tokens.colors?.surface);

  addSection(lines, 'Canonical component aliases');
  addComponentVars(lines, 'primary', button, 'button-primary');
  addComponentVars(lines, 'secondary', secondary, 'button-secondary');
  addComponentVars(lines, 'outline', outline, 'button-outline');
  addComponentVars(lines, 'destructive', destructive, 'button-destructive');
  lines.push('}', '');
  return `${lines.join('\n')}\n`;
}

function addComponentVars(lines, _componentName, component, prefix) {
  if (!component || typeof component !== 'object') return;
  for (const [property, value] of Object.entries(component)) {
    if (property === 'typography' && value && typeof value === 'object') {
      for (const typographyProperty of TYPOGRAPHY_PROPERTIES) {
        if (typographyProperty in value) {
          addVar(lines, `${prefix}-${toKebab(typographyProperty)}`, value[typographyProperty]);
        }
      }
      continue;
    }
    const propertyName = PROPERTY_ALIASES[property] || toKebab(property);
    addVar(lines, `${prefix}-${propertyName}`, value);
  }
}

function addColorAliases(lines, colors) {
  const aliases = {
    'color-page-bg': colors['page-bg'],
    'color-surface': colors.surface,
    'color-surface-muted': colors['surface-muted'],
    'color-surface-subtle': colors['surface-subtle'],
    'color-border': colors.border,
    'color-border-soft': colors['border-soft'],
    'color-on-surface': colors['on-surface'],
    'color-on-heading': colors['on-heading'],
    'color-on-muted': colors['on-muted'],
    'color-on-muted-strong': colors['on-muted-strong'],
    'color-primary': colors.primary,
    'color-primary-strong': colors['primary-strong'],
    'color-primary-soft': colors['primary-soft'],
    'color-focus-ring': colors['focus-ring'],
    'color-success': colors.success,
    'color-success-text': colors['success-text'],
    'color-success-soft': colors['success-soft'],
    'color-warning': colors.warning,
    'color-warning-text': colors['warning-text'],
    'color-warning-soft': colors['warning-soft'],
    'color-error': colors.danger,
    'color-error-text': colors['danger-text'],
    'color-error-soft': colors['danger-soft'],
    'color-danger': colors.danger,
    'color-danger-text': colors['danger-text'],
    'color-danger-soft': colors['danger-soft'],
    'color-info': colors.info,
    'color-info-soft': colors['info-soft'],
    'color-review': colors.review,
    'color-review-soft': colors['review-soft'],
  };

  addSection(lines, 'Semantic color aliases');
  for (const [name, value] of Object.entries(aliases)) {
    addVar(lines, name, value);
  }
}

function addSection(lines, label) {
  lines.push('', `  /* ${label} */`);
}

function addVar(lines, name, value) {
  if (value === undefined || value === null || value === '') return;
  lines.push(`  --${name}: ${cssValue(value)};`);
}

function cssValue(value) {
  if (typeof value === 'number') return String(value);
  if (typeof value === 'string') return value;
  throw new Error(`Cannot serialize non-primitive CSS value: ${JSON.stringify(value)}`);
}

function toKebab(value) {
  return String(value)
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/[^a-zA-Z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .toLowerCase();
}

function writeOrCheck(outputs) {
  let failed = false;
  for (const output of outputs) {
    if (CHECK_MODE) {
      const current = fs.existsSync(output.path) ? fs.readFileSync(output.path, 'utf8') : null;
      if (current !== output.content) {
        process.stderr.write(`design token output is stale: ${output.path}\n`);
        failed = true;
      }
      continue;
    }

    fs.mkdirSync(path.dirname(output.path), { recursive: true });
    fs.writeFileSync(output.path, output.content);
    process.stdout.write(`wrote ${output.path}\n`);
  }

  if (failed) {
    process.stderr.write('Run npm run design:tokens to regenerate committed token outputs.\n');
    process.exit(1);
  }

  if (CHECK_MODE) {
    process.stdout.write('design token outputs are up to date\n');
  }
}

const tokens = readDesignTokens();
writeOrCheck([
  { path: GLOBAL_OUTPUT_PATH, content: buildGlobalCss(tokens) },
  { path: BUTTON_OUTPUT_PATH, content: buildButtonCss(tokens) },
]);
