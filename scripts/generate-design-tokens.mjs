function readDesignTokens() {
  const markdown = fs.readFileSync(DESIGN_PATH, 'utf8');
  const frontMatter = extractFrontMatter(markdown);
  const parsed = parseSimpleYaml(frontMatter);
  if (!parsed || typeof parsed !== 'object') {
    throw new Error(`${DESIGN_PATH} front matter did not parse to an object`);
  }
  return resolveAll(parsed);
}

function extractFrontMatter(markdown) {
  if (!markdown.startsWith('---\n')) {
    throw new Error(`${DESIGN_PATH} must start with YAML front matter`);
  }
  const end = markdown.indexOf('\n---\n', 4);
  if (end === -1) {
    throw new Error(`${DESIGN_PATH} front matter is missing closing delimiter`);
  }
  return markdown.slice(4, end);
}

function parseSimpleYaml(source) {
  const root = {};
  const stack = [{ indent: -1, value: root }];

  for (const [index, rawLine] of source.split(/\r?\n/).entries()) {
    if (!rawLine.trim() || rawLine.trimStart().startsWith('#')) continue;
    const indent = rawLine.match(/^\s*/)?.[0].length ?? 0;
    const line = rawLine.trim();
    const match = line.match(/^(?:"([^"]+)"|'([^']+)'|([^:]+)):\s*(.*)$/);
    if (!match) {
      throw new Error(`${DESIGN_PATH} front matter line ${index + 1} is not supported by the token parser: ${rawLine}`);
    }

    const key = match[1] ?? match[2] ?? match[3].trim();
    const rawValue = match[4];
    while (stack.length > 1 && indent <= stack[stack.length - 1].indent) {
      stack.pop();
    }

    const parent = stack[stack.length - 1].value;
    if (!parent || typeof parent !== 'object' || Array.isArray(parent)) {
      throw new Error(`${DESIGN_PATH} front matter line ${index + 1} cannot attach key ${key}`);
    }

    if (rawValue === '') {
      const child = {};
      parent[key] = child;
      stack.push({ indent, value: child });
      continue;
    }

    parent[key] = parseYamlScalar(rawValue);
  }

  return root;
}

function parseYamlScalar(rawValue) {
  const value = rawValue.trim();
  if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
    return value.slice(1, -1);
  }
  if (/^-?\d+(?:\.\d+)?$/.test(value)) {
    return Number(value);
  }
  return value;
}

function resolveAll(root) {
  const cache = new Map();

  function resolveByPath(referencePath, stack = []) {
    if (cache.has(referencePath)) return cache.get(referencePath);
    if (stack.includes(referencePath)) {
      throw new Error(`Circular design token reference: ${[...stack, referencePath].join(' -> ')}`);
    }
    const value = getByPath(root, referencePath);
    const resolved = resolveValue(value, [...stack, referencePath]);
    cache.set(referencePath, resolved);
    return resolved;
  }

  function resolveValue(value, stack = []) {
    if (typeof value === 'string') {
      const trimmed = value.trim();
      const exactReference = trimmed.match(/^\{([^}]+)\}$/);
      if (exactReference) return resolveByPath(exactReference[1], stack);
      return value.replace(REFERENCE_PATTERN, (_match, referencePath) => {
        const resolved = resolveByPath(referencePath, stack);
        if (resolved && typeof resolved === 'object') {
          throw new Error(`Token reference {${referencePath}} resolves to an object inside a string`);
        }
        return cssValue(resolved);
      });
    }

    if (Array.isArray(value)) {
      return value.map((item) => resolveValue(item, stack));
    }

    if (value && typeof value === 'object') {
      return Object.fromEntries(Object.entries(value).map(([key, item]) => [key, resolveValue(item, stack)]));
    }

    return value;
  }

  return resolveValue(root);
}

function getByPath(root, referencePath) {
  const parts = referencePath.split('.');
  let current = root;
  for (const part of parts) {
    if (!current || typeof current !== 'object' || !(part in current)) {
      throw new Error(`Unknown design token reference: {${referencePath}}`);
    }
    current = current[part];
  }
  return current;
}

function generatedHeader(source = DESIGN_PATH) {
  return [
    '/*',
    ` * Generated by scripts/generate-design-tokens.mjs from ${source}.`,
    ' * Do not edit directly; update DESIGN.md and rerun npm run design:tokens.',
    ' */',
    '',
  ];
}

function buildGlobalCss(tokens) {
  const lines = [...generatedHeader(), ':root {'];
  addSection(lines, 'Colors');
  for (const [name, value] of Object.entries(tokens.colors || {})) {
    addVar(lines, `design-color-${name}`, value);
  }
  addColorAliases(lines, tokens.colors || {});

  addSection(lines, 'Typography');
  for (const [name, typography] of Object.entries(tokens.typography || {})) {
    for (const property of TYPOGRAPHY_PROPERTIES) {
      if (property in typography) {
        addVar(lines, `design-typography-${name}-${toKebab(property)}`, typography[property]);
      }
    }
  }

  addSection(lines, 'Spacing');
  for (const [name, value] of Object.entries(tokens.spacing || {})) {
    addVar(lines, `design-spacing-${name}`, value);
  }

  addSection(lines, 'Radius');
  for (const [name, value] of Object.entries(tokens.rounded || {})) {
    addVar(lines, `design-radius-${name}`, value);
  }

  addSection(lines, 'Borders');
  for (const [name, border] of Object.entries(tokens.borders || {})) {
    if (border && typeof border === 'object') {
      for (const [property, value] of Object.entries(border)) {
        addVar(lines, `design-border-${name}-${toKebab(property)}`, value);
      }
      if ('width' in border && 'style' in border && 'color' in border) {
        addVar(lines, `design-border-${name}`, `${cssValue(border.width)} ${cssValue(border.style)} ${cssValue(border.color)}`);
      }
    } else {
      addVar(lines, `design-border-${name}`, border);
    }
  }

  addSection(lines, 'Focus');
  for (const [name, focus] of Object.entries(tokens.focus || {})) {
    if (focus && typeof focus === 'object') {
      for (const [property, value] of Object.entries(focus)) {
        addVar(lines, `design-focus-${name}-${toKebab(property)}`, value);
      }
      if ('width' in focus && 'color' in focus) {
        addVar(lines, `design-focus-${name}`, `${cssValue(focus.width)} solid ${cssValue(focus.color)}`);
      }
    } else {
      addVar(lines, `design-focus-${name}`, focus);
    }
  }

  addSection(lines, 'Opacity');
  for (const [name, value] of Object.entries(tokens.opacity || {})) {
    addVar(lines, `design-opacity-${name}`, value);
  }

  addSection(lines, 'Layers');
  for (const [name, value] of Object.entries(tokens.layers || {})) {
    addVar(lines, `design-layer-${name}`, value);
  }

  addSection(lines, 'Shadows');
  for (const [name, value] of Object.entries(tokens.shadows || {})) {
    addVar(lines, `design-shadow-${name}`, value);
  }

  addSection(lines, 'Components');
  for (const [name, component] of Object.entries(tokens.components || {})) {
    addComponentVars(lines, name, component, `design-component-${name}`);
  }

  lines.push('}');
  return `${lines.join('\n')}\n`;
}

